import mongoose from "mongoose";

const UserSchema = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    lastName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    username: {
      type: String,
      required: true,
      max: 50,
      unique: true,
    },
    password: {
      type: String,
      required: true,
      min: 5,
    },
    picturePath: {
      type: String,
      default: "",
    },
    friends: {
      type: Array,
      default: [],
    },
    dob: String,
    height: String,
    weight: String,
    handedness: String,
    sport: String,
    position: String,
    team: String,
    hometown: String,
  },
  { timestamps: true }
);

const User = mongoose.model("User", UserSchema);
export default User;
